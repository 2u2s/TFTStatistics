# HANDLING RECOMMENDED ITEM DATA
# input: match_info.Which_item_favored (dict)
# output: dict
def recommended_item_sorting(data):
    output = {}
    for chmp in data:
        r_item = data[chmp].items()
        r_item_sorted = sorted(r_item, key=lambda x: x[1], reverse=True)
        ##output[chmp] = r_item_sorted #it is just sorting
        freq_sum = 0
        output[chmp] = {}
        cnt = 0
        for i in range(len(r_item_sorted)):
            if r_item_sorted[i][0] < 10:
                continue

            freq_sum += r_item_sorted[i][1]

            if cnt >= 5:
                continue
            else:
                output[chmp][str(r_item_sorted[i][0])] = r_item_sorted[i][1]
                cnt += 1
            output[chmp]["sum"] = freq_sum
    return output


def get_galaxy_data(data):
    output = {}
    total = get_datas.make_meta_dict()
    for galaxy in data:
        output[galaxy] = get_datas.make_meta_dict()
        for g_deck_name in data[galaxy]:
            g_deck = data[galaxy][g_deck_name]
            if g_deck["game_cnt"] == 0:
                continue
            output[galaxy][g_deck_name]["game_cnt"] = g_deck["game_cnt"]
            output[galaxy][g_deck_name]["win"] = round(g_deck["win"] / g_deck["game_cnt"], ndigits=4)
            output[galaxy][g_deck_name]["top4"] = round(g_deck["top4"] / g_deck["game_cnt"], ndigits=4)
            output[galaxy][g_deck_name]["mean_rank"] = round(g_deck["rank_sum"] / g_deck["game_cnt"], ndigits=4)
            output[galaxy][g_deck_name]["completion_rate"] = round(g_deck["completed_cnt"] / g_deck["game_cnt"],
                                                                   ndigits=4)
            output[galaxy][g_deck_name].pop("rank_sum")

            # get total data
            total[g_deck_name]["win"] += g_deck["win"]
            total[g_deck_name]["top4"] += g_deck["top4"]
            total[g_deck_name]["rank_sum"] += g_deck["rank_sum"]
            total[g_deck_name]["game_cnt"] += g_deck["game_cnt"]
            total[g_deck_name]["completed_cnt"] += g_deck["completed_cnt"]
            total[g_deck_name]["completed_win"] += g_deck["completed_win"]
            total[g_deck_name]["completed_top4"] += g_deck["completed_top4"]
            total[g_deck_name]["completed_rank_sum"] += g_deck["completed_rank_sum"]

            # handling data when deck is completed
            if g_deck["completed_cnt"] == 0:
                continue
            output[galaxy][g_deck_name]["completed_win"] = round(g_deck["completed_win"] / g_deck["completed_cnt"],
                                                                 ndigits=4)
            output[galaxy][g_deck_name]["completed_top4"] = round(g_deck["completed_top4"] / g_deck["completed_cnt"],
                                                                  ndigits=4)
            output[galaxy][g_deck_name]["completed_mean_rank"] = round(
                g_deck["completed_rank_sum"] / g_deck["completed_cnt"], ndigits=4)

    for deck_name in total:
        deck = total[deck_name]
        if deck["game_cnt"] == 0:
            continue
        deck["win"] = round(deck["win"] / deck["game_cnt"], ndigits=4)
        deck["top4"] = round(deck["top4"] / deck["game_cnt"], ndigits=4)
        deck["rank_mean"] = round(deck["rank_sum"] / deck["game_cnt"], ndigits=4)
        deck["completion_rate"] = round(deck["completed_cnt"] / deck["game_cnt"], ndigits=4)
        deck.pop("rank_sum")
        if deck["completed_cnt"] == 0:
            continue
        deck["completed_win"] = round(deck["completed_win"] / deck["completed_cnt"], ndigits=4)
        deck["completed_top4"] = round(deck["completed_top4"] / deck["completed_cnt"], ndigits=4)
        deck["completed_mean_rank"] = round(deck["completed_rank_sum"] / deck["completed_cnt"], ndigits=4)
    output["total"] = total

    return output



def _make_zeros(n):
    output = []
    for i in range(n):
        output.append(0)
    return output


def _std_dot(v1, v2):
    inner_product = 0
    v1_sq = 0
    v2_sq = 0
    if len(v1) != len(v2):
        print("vector size is different!!!")
        raise ValueError
    else:
        n = len(v1)
        for i in range(n):
            inner_product += v1[i] * v2[i]
            v1_sq += v1[i] * v1[i]
            v2_sq += v2[i] * v2[i]
        std_ip = inner_product / ((v1_sq ** 0.5) * (v2_sq ** 0.5))
        std_ip = round(std_ip * 1000) / 1000
        return std_ip


